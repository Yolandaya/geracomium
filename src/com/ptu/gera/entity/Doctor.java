package com.ptu.gera.entity;

public class Doctor {
    private Integer doctor_id;
    private String doctor_name;
    private String doctor_dir;
    private String doctor_img;

    public Integer getDoctor_id() {
        return doctor_id;
    }

    public void setDoctor_id(Integer doctor_id) {
        this.doctor_id = doctor_id;
    }

    public String getDoctor_name() {
        return doctor_name;
    }

    public void setDoctor_name(String doctor_name) {
        this.doctor_name = doctor_name;
    }

    public String getDoctor_dir() {
        return doctor_dir;
    }

    public void setDoctor_dir(String doctor_dir) {
        this.doctor_dir = doctor_dir;
    }

    public String getDoctor_img() {
        return doctor_img;
    }

    public void setDoctor_img(String doctor_img) {
        this.doctor_img = doctor_img;
    }
}
